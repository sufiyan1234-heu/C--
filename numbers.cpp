#include <iostream>
using namespace std;
int main()
{
    int wnum = 5;            // whole numbers
    double dnum = 5.5;       // double numbers
    wnum++;                  // short hand for increment of one
    wnum += 80;              // short hand to add 80 to the wnum and you can do +,*,-,/
    cout << 5.5 / 8 << endl; // dnum / wnum = dnum
    cout << 5 / 2 << endl;   // wnum / wnum =  wnum
    cout << wnum;
}