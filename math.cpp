#include <iostream>
#include <cmath> //to perform maths operations
using namespace std;
int main()
{
    cout << pow(2, 5) << endl;  // to find power of the number first argument is the number and second is the power
    cout << round(4.3) << endl; // to round off the number
    cout << ceil(4.1) << endl;  // round up to the first higest whole number
    cout << floor(4.8) << endl; // round down to the whole number
    cout << fmax(4, 7) << endl; // tells the maximum number
    cout << fmin(4, 5) << endl; // tells the minimum number
}